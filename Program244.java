import java.util.*;
class Program244
{
    public static int Factorial(int iNo)
    {
        int i=0;
        int iRet = 1;
        for(i=1;i<=iNo;i++)
        {
            iRet = iRet * i;

        }
        return iRet;

    }
   
    public static void main(String Arg[])
    {
       
        Scanner sobj = new Scanner(System.in);

        int iValue = 0,iAns =0;

        System.out.println("Enter number : ");
        iValue = sobj.nextInt();
        iAns = Factorial(iValue);

        System.out.println("Factorial is :"+iAns);
        sobj.close();

       


    }
    
}

